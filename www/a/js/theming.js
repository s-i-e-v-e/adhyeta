function build_icon(x) {
	return `<a class="icon" href="javascript:void(0)">${x}</a>`
}

function theming_init() {
	const mode = load_config().scheme

	qs("#scheme").innerHTML = build_icon(mode === "light" ? SVG_SCHEME_LIGHT : SVG_SCHEME_DARK)
	qs("#theme").innerHTML = build_icon(SVG_PALETTE)
	qs("#font").innerHTML = build_icon(SVG_FONT)
	qs("#account").innerHTML = build_icon(SVG_ACCOUNT)
	if (!qs(".app")) {
		qs("#account a").setAttribute("href", "https://app.adhyeta.org.in")
	}

	qs("#scheme a").addEventListener("click", (e) => {
		toggle_mode();
	}, true);

	qs("#theme a").addEventListener("click", (e) => {
		e.stopPropagation();
		theme_show_menu();
	}, true);

	qs("#font a").addEventListener("click", (e) => {
		e.stopPropagation();
		font_show_menu();
	}, true);

	qs("#account a").addEventListener("click", (e) => {
		if (e.target.href.startsWith("http")) {
			return true
		}
		e.stopPropagation();
		const session = qs("#session")
		if (session && session.attributes["is-session"].value === "1") {
			clear_all_menus()
			const x = new_el("div", {"class": "context"}, `<li><a href="/logout">logout</a></li>`)
			qs("#ephemeral").appendChild(x);
			qs("#ephemeral").addEventListener("click", e => {
				e.stopPropagation();
			});
			htmx.process(x)
		}
	}, true);


	set_mode(mode);
	theme_update();
	font_update();
}

